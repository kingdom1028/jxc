package com.kingdom.store.model;

public class TSob {
	private String sobName;
	private String sobCompanyName;
	private String sobId;
	private String sobState;
	public String getSobName() {
		return sobName;
	}
	public void setSobName(String sobName) {
		this.sobName = sobName;
	}
	public String getSobCompanyName() {
		return sobCompanyName;
	}
	public void setSobCompanyName(String sobCompanyName) {
		this.sobCompanyName = sobCompanyName;
	}
	public String getSobId() {
		return sobId;
	}
	public void setSobId(String sobId) {
		this.sobId = sobId;
	}
	public String getSobState() {
		return sobState;
	}
	public void setSobState(String sobState) {
		this.sobState = sobState;
	}
	
}
